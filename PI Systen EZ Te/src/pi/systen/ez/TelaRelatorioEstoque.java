    private DefaultTableModel montarTabela() {
        String[] colunas = {"ID", "NOME DO PRODUTO", "QUANTIDADE NO ESTOQUE"};
        
        DefaultTableModel tabela = new DefaultTableModel(colunas, 0);
        List<Produto> lista = ProdutoDAO.listarEstoque();
        
        for (int i = 0; i < lista.size(); i++) {
            Produto p = lista.get(i);
            
            String[] linhas = {
                String.valueOf(p.getId()),
                p.getNome(),
                String.valueOf(p.getQntdEstoque())
                    
            };
            
            tabela.addRow(linhas);
        }
        
        return tabela;
    }
    
    private DefaultTableModel montarTabelaBusca(String busca) {
        ProdutoDAO prodsDAO = new ProdutoDAO();
        String[] colunas = {"ID", "NOME DO PRODUTO", "QUANTIDADE NO ESTOQUE"};

        DefaultTableModel tabela = new DefaultTableModel(colunas, 0);

        List<Produto> lista = ProdutoDAO.buscarPorNomeProdutoR(busca);

        for (int i = 0; i < lista.size(); i++) {
            Produto p = lista.get(i);

            String[] linha = {
                String.valueOf(p.getId()),
                p.getNome(),
                String.valueOf(p.getQntdEstoque()),
            };
            tabela.addRow(linha);
        }

        if(tabela.getRowCount() == 0) {
            JOptionPane.showMessageDialog(null, "Nenhum produto encontrado com esse nome!", "Busca", JOptionPane.INFORMATION_MESSAGE);
        }
        
        return tabela;
    }

