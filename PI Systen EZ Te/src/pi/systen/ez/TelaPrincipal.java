    private void configurarAcesso() {

        if(usuario.getTipo().equalsIgnoreCase("Administrador")){
            
            btnRegistro.setEnabled(true);
            btnCadastros.setEnabled(true);
            btnListas.setEnabled(true);
            btnRelatorios.setEnabled(true);
            
        } else if(usuario.getTipo().equalsIgnoreCase("Gerente de Estoque")) {
            
            btnRegistro.setEnabled(true);
            btnCadastros.setEnabled(true);
            btnListas.setEnabled(true);
            btnRelatorios.setEnabled(true);
            
        } else if(usuario.getTipo().equalsIgnoreCase("Vendedor")){
            btnRegistro.setEnabled(true);
            btnCadastros.setEnabled(false);
            btnListas.setEnabled(true);
            btnRelatorios.setEnabled(true);
        }
    }

